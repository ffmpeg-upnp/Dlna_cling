package com.wsd.sun.myapplication.model;



public class MediaVideo {
  private int intVideoDuration;
  private String strVideoName;
  private String strVideoPath;

  /**
   * @return the intVideoDuration
   */
  public int getIntVideoDuration() {
    return intVideoDuration;
  }

  /**
   * @param intVideoDuration the intVideoDuration to set
   */
  public void setIntVideoDuration(int intVideoDuration) {
    this.intVideoDuration = intVideoDuration;
  }

  /**
   * @return the strVideoName
   */
  public String getStrVideoName() {
    return strVideoName;
  }

  /**
   * @param strVideoName the strVideoName to set
   */
  public void setStrVideoName(String strVideoName) {
    this.strVideoName = strVideoName;
  }

  /**
   * @return the strVideoPath
   */
  public String getStrVideoPath() {
    return strVideoPath;
  }

  /**
   * @param strVideoPath the strVideoPath to set
   */
  public void setStrVideoPath(String strVideoPath) {
    this.strVideoPath = strVideoPath;
  }



}
